#!/usr/bin/env python3
'''
## 0. Basic Annotations - Add
Write a type-annotated function ```add``` that takes
a float ```a``` and a float ```b``` as arguments
and returns their sum as a float.
'''

def add(a: float, b: float) -> float:
    '''
    Type-annotated function that takes two
    floats and returns their sum as a float

    @a: First float
    @b: Second float
    Return: Sum of variables, represented as a float
    '''
    return a + b
