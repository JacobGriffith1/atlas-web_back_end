#!/usr/bin/env python3
'''
## 3. Basic Annotations - To String
Write a type-annotated function ```to_str```
that takes a float ```n``` as an argument
and returns the string representation of the float.
'''


def to_str(n: float) -> str:
    '''
    Type-annotated function that takes a float
    and returns it as a str

    @n: Float
    Return: String representation of n
    '''
    return str(n)
